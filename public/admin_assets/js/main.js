  function calculate() {
    var lengthFeet = parseFloat(document.getElementById("input1").value);
    var widthFeet = parseFloat(document.getElementById("input2").value);

    // Conversion factor from feet to marla
    var conversionFactor = 1 / 272.25;

    // Calculate area in square feet
    var areaFeet = lengthFeet * widthFeet;

    // Convert area to marla
    var areaMarla = areaFeet * conversionFactor;

    // Display the result
    document.getElementById("result").value = areaMarla.toFixed(2) + " marla";
}

